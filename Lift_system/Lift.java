public class Lift {
   int floor_no;
   int capacity;
   Lift(int floor_no,int capacity) {
    this.floor_no=floor_no;
    this.capacity=capacity;
   }
public int getFloor_no() {
    return floor_no;
}
public void setFloor_no(int floor_no) {
    this.floor_no = floor_no;
}
public int getCapacity() {
    return capacity;
}
public void setCapacity(int capacity) {
    this.capacity = capacity;
}

}
